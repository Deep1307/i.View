<br>


It is a universally recognised fact that every individual bears a different moral character and a different set of personality traits. Despite this, it is also understood that coexisting with these differences are similarities that bind us together as people. True Colors, for instance, aims to understand and identify these personality traits and associate them with colours. Classifying personality types into different categories and subcategories is a method long worked on by several psychologists and researchers. It also depicts the works of author Katharine Cook Briggs, and her daughter Isabel Briggs-Myers along with psychologist David Keirsey. True Colors was originally instigated by a student of Professor Keirsey, Don Lowry. It used the four primary colours to identify personality styles and behavioural traits.
<br><br>
Don initially developed True Colors to pioneer understanding of differing personality styles and enhance quality of communication in an attempt to reinforce positive response and give a boost to every individual's self-worth. It was also meant be psychologically applied in professional fields like education, workplaces, etc. so as to improve interaction.
<br><br>
This comes from the simple ideology that with a deeper understanding of the human personality, chances of conflict can drop down drastically as we attempt to understand the difference in behaviour, ethics, perspectives, etc. present in every individual.
<br><br>
Finally, how it functions is quite simple, yet effective. True Colors, in a way, paints all your major personality styles as different colours. This doesn't necessarily mean every person is restricted to simply one colour. Much like the fluidity of our perspectives, it goes without saying that every individual may have a small part of every personality type despite a single style being predominantly visible.
<br><br>
